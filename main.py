# Лабораторная работа №2
# Вариант 21
# Визуализация и спектральный анализ речевого сигнала

import re
import time
import wave

import matplotlib.pyplot as plt
import numpy as np


# Запоминаем время начала работы программы.
# В конце программы это понадобится для вывода времени выполнения.
start_time = time.time()

print("Лабораторная работа №2")
print("Вариант 21")
print("Программа выполняет визуализацию и спектральный анализ wav-файла.")
print("Для варианта 21 требуется wav-файл с частотой дискретизации 32000 Гц.")
print("Файл должен называться: 21.wav\n")


# Регулярное выражение проверяет корректность имени wav-файла.
# Разрешены буквы, цифры, пробелы, дефисы и нижние подчеркивания.
# В конце имени обязательно должно быть расширение .wav
file_name_pattern = r"^[\wА-Яа-яЁё\s\-]+\.wav$"


# Запрашиваем имя файла до тех пор, пока пользователь не введет
# строку, похожую на имя wav-файла.
while True:
    file_name = input("Введите имя wav-файла, например 21.wav: ").strip()

    if re.match(file_name_pattern, file_name):
        break

    print("Ошибка: имя файла должно оканчиваться на .wav")


# Количество отсчетов нужно только для первого графика.
# Пользователь сам выбирает, сколько первых отсчетов сигнала показать.
while True:
    try:
        samples_count = int(
            input("Введите количество отсчетов для первого графика: ")
        )

        if samples_count > 0:
            break

        print("Ошибка: количество отсчетов должно быть положительным.")
    except ValueError:
        print("Ошибка: введите целое число.")


# Открываем wav-файл и считываем его основные параметры:
# количество каналов, размер одного отсчета, частоту дискретизации
# и общее количество звуковых кадров.
try:
    with wave.open(file_name, "rb") as wav_file:
        channels_count = wav_file.getnchannels()
        sample_width = wav_file.getsampwidth()
        sample_rate = wav_file.getframerate()
        frames_count = wav_file.getnframes()

        # Считываем все звуковые данные из файла в виде байтов.
        frames = wav_file.readframes(frames_count)

except FileNotFoundError:
    print("\nОшибка: файл не найден.")
    print("Проверьте, что wav-файл находится в одной папке с программой.")
    print(time.time() - start_time, "seconds")
    raise SystemExit

except wave.Error:
    print("\nОшибка: файл не является корректным wav-файлом.")
    print(time.time() - start_time, "seconds")
    raise SystemExit


# Выводим информацию о файле, чтобы пользователь видел,
# какие параметры реально имеет выбранный wav-файл.
print("\nИнформация о wav-файле:")
print("Количество каналов:", channels_count)
print("Размер отсчета в байтах:", sample_width)
print("Частота дискретизации:", sample_rate, "Гц")
print("Количество отсчетов:", frames_count)


# По заданию для варианта 21 нужна частота дискретизации 32000 Гц.
# Если файл имеет другую частоту, программа не падает,
# а предупреждает пользователя и продолжает работу.
if sample_rate != 32000:
    print("\nПредупреждение:")
    print("Для варианта 21 требуется частота дискретизации 32000 Гц.")
    print("Текущий файл имеет частоту", sample_rate, "Гц.")
    print("Программа продолжит работу с фактической частотой файла.")


# По заданию нужен mono-файл.
# Если файл оказался stereo или многоканальным,
# программа возьмет только первый канал для анализа.
if channels_count != 1:
    print("\nПредупреждение:")
    print("По заданию нужен mono-файл.")
    print("Текущий файл содержит", channels_count, "канала.")
    print("Для анализа будет использован первый канал.")


# Лабораторная требует файл PCM signed 16 bit.
# Для такого формата один отсчет занимает 2 байта.
# Если формат другой, программа аккуратно завершает работу.
if sample_width != 2:
    print("\nОшибка:")
    print("Программа рассчитана на wav-файлы PCM signed 16 bit.")
    print("В данном файле размер отсчета равен", sample_width, "байт.")
    print(time.time() - start_time, "seconds")
    raise SystemExit


# Преобразуем байтовые данные wav-файла в массив чисел.
# dtype=np.int16 соответствует формату signed 16 bit.
signal = np.frombuffer(frames, dtype=np.int16)


# Если в файле несколько каналов, данные идут по очереди:
# левый канал, правый канал, левый канал, правый канал и т.д.
# Поэтому сначала преобразуем массив в таблицу каналов,
# а затем оставляем только первый канал.
if channels_count > 1:
    signal = signal.reshape(-1, channels_count)
    signal = signal[:, 0]


# Если пользователь запросил больше отсчетов, чем есть в файле,
# программа не завершится с ошибкой, а построит все доступные отсчеты.
if samples_count > len(signal):
    print("\nПредупреждение:")
    print("В файле меньше отсчетов, чем было запрошено.")
    print("Будут построены все доступные", len(signal), "отсчетов.")
    samples_count = len(signal)


# Номера отсчетов нужны для первого графика.
# По оси X будет номер отсчета, по оси Y — амплитуда.
sample_numbers = np.arange(samples_count)


# Для осциллограммы нужно перейти от номеров отсчетов ко времени.
# Время одного отсчета равно номеру отсчета, деленному на частоту дискретизации.
time_values = np.arange(len(signal)) / sample_rate


# Выполняем дискретное преобразование Фурье для всего сигнала.
# Результат ДПФ содержит комплексные числа: Re + j * Im.
spectrum = np.fft.fft(signal)


# Получаем частоты, соответствующие значениям ДПФ.
# Частоты нужны для правильной подписи горизонтальной оси спектра.
frequencies = np.fft.fftfreq(len(signal), d=1 / sample_rate)


# Для действительного звукового сигнала спектр симметричен,
# поэтому обычно отображают только положительную половину частот.
positive_frequencies = frequencies[:len(frequencies) // 2]
positive_spectrum = spectrum[:len(spectrum) // 2]


# Для варианта 21 требуется модуль ДПФ.
# Формула: sqrt(Re**2 + Im**2).
# np.abs для комплексных чисел как раз вычисляет этот модуль.
spectrum_module = np.abs(positive_spectrum)


# Создаем одно окно с четырьмя графиками,
# так как по заданию нужно построить результаты пунктов 1.1-1.4.
plt.figure(figsize=(14, 10))


# Первый график:
# отображает заданное пользователем количество первых отсчетов.
# Для варианта 21 требуется линейный сплошной график.
plt.subplot(2, 2, 1)
plt.plot(sample_numbers, signal[:samples_count], linestyle="-")
plt.title("Первые отсчеты звукового сигнала")
plt.xlabel("Номер отсчета")
plt.ylabel("Амплитуда, у.е.")
plt.grid(True)


# Второй график:
# осциллограмма всего звукового сигнала как функция времени.
plt.subplot(2, 2, 2)
plt.plot(time_values, signal, linestyle="-")
plt.title("Осциллограмма звукового сигнала")
plt.xlabel("Время, с")
plt.ylabel("Амплитуда, у.е.")
plt.grid(True)


# Третий график:
# результат спектрального анализа.
# По оси X — частота в Герцах, по оси Y — модуль ДПФ.
plt.subplot(2, 2, 3)
plt.plot(positive_frequencies, spectrum_module, linestyle="-")
plt.title("Спектральный анализ: модуль ДПФ")
plt.xlabel("Частота, Гц")
plt.ylabel("Модуль ДПФ, у.е.")
plt.grid(True)


# Четвертый график:
# гистограмма показывает, как часто значения отсчетов
# попадают в разные амплитудные интервалы.
plt.subplot(2, 2, 4)
plt.hist(signal, bins=50)
plt.title("Гистограмма отсчетов звукового сигнала")
plt.xlabel("Амплитудный интервал, у.е.")
plt.ylabel("Количество отсчетов")
plt.grid(True)


# Автоматически улучшаем расположение графиков,
# чтобы подписи и названия не накладывались друг на друга.
plt.tight_layout()

print(time.time() - start_time, "seconds")
# Показываем окно со всеми построенными графиками.
plt.show()


print("\nПрограмма успешно завершена.")
